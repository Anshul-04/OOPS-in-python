# The objects in the list can be iterated over one by one, by using a for a loop.

my_list = [5, 8, 'Tom', 7.50, 'Emma']

# iterate a list
for item in my_list:
    print(item)

# The index value starts from 0 to (length of the list-1)


my_list2 = ['A','B','C',1,2,3]
# iterate a list
for i in range(2, 4):
    # print each item using index number
    print(my_list2[i])